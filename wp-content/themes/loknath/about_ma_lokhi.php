							<p>Baba Lokenath Brahmachari was born on Janmastami, the birthday of Lord Krishna, on 18th Bhadra, 1137 Bengali Era (roughly 29th August 1730) to a Brahmin family in the village of Chaurasi Chakla, named as Kochua, a couple of miles from Kolkata.
							His father Shree Ramnarayan Ghoshal and mother Shrimati Kamaladevi were both wholeheartedly God's true devotee. Ramnarayan Ghosal's one wish in life was to dedicate a child to the path of renunciation to liberate the family. Ramnarayan expressed his wish to his beloved wife and she agreed to stand beside her husband on this pious decision. Eventually when Kamaladevi became pregnant and gave birth to a beautiful son, Ramnarayan reminded her about their decision. Poor mother was in tears and begged him not to give away this child but also promised to dedicate the next one. Thus the couple had three sons, but none of them could be taken away from the mother Kamaladevi's lap, and Ramnarayan's wish remained unfulfilled</p>
							<p>Ramnarayan understood the affection of a mother's heart and decided to wait for the moment when his wife will dedicate a son on her own. Finally when the fourth son was born, his wife Kamaladevi willingly handed him over to her husband. Ramnarayan knew that the time had come to initiate his boy to the service of God. He pleaded with Acharya Bhagawan Ganguly, a famous Pandit and renowned scholar of that time in the village of Kochuya, to be his son's Guru and show him the way of "Moksha" or enlightenment. Guru Bagawan Ganguly agreed to accept Lokenath as his one and only disciple as he waited for this moment for his entire life. But an unavoidable circumstance occurred when Lokenath's childhood friend Sree Benimadhab urged to take him with them. Lokenath and his Guru both failed to this young boy's strong will to accept "Sanyasa" as his way of life, therefore they had to take him with them as well. At the age of 11, young Lokenath left home with his guru. He visited Kalighat Temple in Kolkata and then lived in the forests for 25 years, selflessly serving his master and practicing the Ashtanga Yoga of Patanjali along with the most difficult Hatha Yoga. After this he travelled to the Himalayas where he meditated in the nude for nearly five decades. Finally, he attained enlightenment at the age of ninety</p>
							<p>After his enlightenment he traveled extensively on foot to Afghanistan, Persia, Arabia and Israel, making three pilgrimages to Mecca. When he came to the small town Baradi near Dhaka, a wealthy family built him a small hermitage, which became his ashram.There he accepted the sacred thread of the Brahmins and clothed himself in saffron robes. For the rest of his life he performed miracles and gave divine wisdom to all who came to him to seek his blessings. He received the title Baba (Bengali for Father).</p>
							<p>On the 19th day of Jaistha, 1297 (1st June, 1890), Sunday, at 11:45 am, Baba Lokenath was meditating when he went into a trance with his eyes open, and while still in meditation, left his physical body forever. He was aged 160. He had made a promise before his death</p>
